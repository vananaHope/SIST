 # 제3정규화
 1. 이행적 함수 종속성을 제거하는 것을 말한다.
 2. 이행적 함수 종속성 
 		릴레이션을 구성하는 3개의 속성 집합 x,y,z에 대해 함수 종속 관계
 		x->y와 y->z가 존재하면 논리적으로 x->z가 성립되는데
 		이 때 z가 x에 이행적으로 함수 종속되었다고 함
 		ex) 사원정보 + 부서정보
 		사원명을 통해서 부서명을 알 수 있고, 부서명을 통해 부서 위치를
 		알 수 있으면 사원번호를 통해서 부서위치를 알 수 있을 떄
 		부서위치는 사원번호에 이행적으로 함수 종속되었다고 한다.
 		
 		# 사원정보 ==> 부서정보를 알 수 있지만(단일 데이터)
 		  부서정보 ==> 사원정보를 알 수 없다.(여러 개 데이터가 나올 수 있다)
 		
 		
