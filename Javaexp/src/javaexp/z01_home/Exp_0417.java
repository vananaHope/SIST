package javaexp.z01_home;

public class Exp_0417 {
	
	private String gender;
	private String name;
	private int age1;
	
	public Exp_0417(String gender, String name, int age1) {
		this.gender = gender;
		this.name = name;
		this.age1 = age1;
	}

	public String getGender() {
		return gender;
	}

	public void setGender(String gender) {
		this.gender = gender;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getAge1() {
		return age1;
	}

	public void setAge1(int age1) {
		this.age1 = age1;
	}
}
